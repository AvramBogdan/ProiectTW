			 <h4>5. KLM</h4>
	<ul>
    <li>Țară: Olanda</li>
    <li>Cod: KL, KLM</li>	
	</ul>
	<p>Fondată în 1919, la cererea Reginei Wilhelmina, KLM este cea mai veche companie aeriană din lume care operează 
	în continuare sub numele inițial. Inițialele numelui provin de la Koninklijke Luchtvaart Maatschappij, tradus prin 
